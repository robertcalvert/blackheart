package blackheart;

/**
 *
 * @author rob
 */
public final class StateSleep implements IState {

    private final String id = "SLEEP";
    private final Core _core;

    public StateSleep(Core core) {
        _core = core;
    }

    @Override
    public String id() {
        return id;
    }

    @Override
    public void tick() throws Exception {
        if (!(_core.state().equals(this))) {
            _core.state(this);
        }
        try {
            Thread.sleep(100);
        } catch (InterruptedException e) {
        }
    }
}